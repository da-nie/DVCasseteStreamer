//----------------------------------------------------------------------------------------------------
//задать секцию Video
//----------------------------------------------------------------------------------------------------
void CDVVideo::Set(SVideo *sVideo_Ptr,uint8_t dif_sequence,uint8_t block_number)
{
 //заполняем каждый блок DIF
 cDVDIFID.SetVideoSectionID(&sVideo_Ptr->sDIFID,dif_sequence,block_number);
}
//----------------------------------------------------------------------------------------------------
//вывести в файл параметры секции
//----------------------------------------------------------------------------------------------------
void CDVVideo::OutputToFile(FILE *file,const std::string &prefix,SVideo *sVideo_Ptr)
{
 if (file==NULL) return;
 CDVDIFID::SDIFID *sDIFID_Ptr=&sVideo_Ptr->sDIFID;
 cDVDIFID.OutputToFile(file,prefix,sDIFID_Ptr);
 fprintf(file,"\r\n");
}
